def get_reply_keyboard():
    """–°–æ–∑–¥–∞–µ—Ç reply keyboard —Å –æ—Å–Ω–æ–≤–Ω—ã–º–∏ –∫–Ω–æ–ø–∫–∞–º–∏"""
    keyboard = [
        ['‚≠ê –ö—É–ø–∏—Ç—å –∑–≤–µ–∑–¥—ã', 'üíé –ö—É–ø–∏—Ç—å Premium'],
        ['üë§ –ü—Ä–æ—Ñ–∏–ª—å', 'üÜò –ü–æ–¥–¥–µ—Ä–∂–∫–∞']
    ]
    return ReplyKeyboardMarkup(
        keyboard,
        resize_keyboard=True,
        one_time_keyboard=False
    )


